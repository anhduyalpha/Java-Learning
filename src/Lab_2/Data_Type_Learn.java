package Lab_2;

public class Data_Type_Learn {
    public static void data_type_leaning(String[] args) {
        // Primitive Data Types
        byte a = 100; // Stores whole numbers from -128 to 127
        double b = 820.2304923; // Storing 15 decimal digits
        int c = 192381023; // Stores whole numbers from -2,147,483,648 to 2,147,483,647
        boolean d = true;
        char e = 'a'; // Single character
        String f = "abc"; //
        // more like long, short, float,...

        // Java math operators
        int g = a + a;
        int h = a - a;
        int j = a % a;
        int k = c++; // increase 1
        int l = c--; // decrease 1
        // more like >, <, >=, <=, ==, !=

        // Java conditional operators
        // && and
        // || or
        // ! not
    }


}
